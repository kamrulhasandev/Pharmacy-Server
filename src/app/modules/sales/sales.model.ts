import { Schema, model } from "mongoose";
import { TSales } from "./sales.interface";

const saleModel = new Schema<TSales>(
  {
    product: {
      type: Schema.Types.ObjectId,
      ref: "Product",
      required: true,
    },
    user: {
      type: Schema.Types.ObjectId,
      ref: "User",
      required: true,
    },
    quantity: {
      type: Number,
      required: true,
    },
    price: {
      type: Number,
      required: true,
    },
  },
  { timestamps: true }
);

export const Sale = model<TSales>("Sale", saleModel);
